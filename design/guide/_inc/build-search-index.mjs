#!/usr/bin/env node
/**
 * Extracts searchable text from the three guide HTML pages and writes
 * search-index.js — a static index that works with file:// (no fetch).
 *
 * Usage:  node design/guide/_inc/build-search-index.mjs
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { JSDOM } from 'jsdom';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const guideDir = resolve(__dirname, '..');

const PAGES = [
  { file: 'layout.html',      title: 'Design Tokens' },
  { file: 'components.html',  title: 'Component Library' },
  { file: 'iconography.html', title: 'Iconography' },
];

const SELECTOR = 'p, h1, h2, h3, h4, li, td, th, .card__title, .card__label, .card__value, .card__desc, .section__description, .page__subtitle';

function slugify(text) {
  return text.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '');
}

const index = PAGES.map(page => {
  const html = readFileSync(resolve(guideDir, page.file), 'utf-8');
  const dom = new JSDOM(html);
  const doc = dom.window.document;

  /* Collect sections with their searchable text */
  const sections = [];
  const sectionEls = doc.querySelectorAll('.section, section, [is="token-section"]');

  /* Also handle Vue template-rendered token-section elements by title attr */
  const tokenSections = doc.querySelectorAll('token-section');

  /* Build a list of sections from both regular and token-section elements */
  const allSections = [...sectionEls, ...tokenSections];

  if (allSections.length === 0) {
    /* Fallback: treat the whole page body as one section */
    const els = doc.querySelectorAll(SELECTOR);
    const texts = [];
    els.forEach(el => {
      const t = el.textContent.trim();
      if (t) texts.push(t);
    });
    if (texts.length) {
      sections.push({ section: '', sectionId: '', text: texts.join(' ') });
    }
  } else {
    allSections.forEach(secEl => {
      const heading = secEl.querySelector('.section__title, h2');
      const titleAttr = secEl.getAttribute('title');
      const secName = (heading ? heading.textContent : titleAttr || '').trim();
      const secId = secName ? slugify(secName) : '';

      const els = secEl.querySelectorAll(SELECTOR);
      const texts = [];
      els.forEach(el => {
        const t = el.textContent.trim();
        if (t) texts.push(t);
      });

      /* Also grab text from heading and description attrs for token-section */
      const desc = secEl.getAttribute('description');
      if (secName) texts.unshift(secName);
      if (desc) texts.unshift(desc);

      if (texts.length) {
        sections.push({ section: secName, sectionId: secId, text: texts.join(' ') });
      }
    });
  }

  return { file: page.file, title: page.title, sections };
});

const output = `/* Auto-generated by build-search-index.mjs — do not edit */\nwindow.GUIDE_SEARCH_INDEX = ${JSON.stringify(index, null, 2)};\n`;
const outPath = resolve(__dirname, 'search-index.js');
writeFileSync(outPath, output, 'utf-8');
console.log('Wrote', outPath, '(' + (output.length / 1024).toFixed(1) + ' KB)');
